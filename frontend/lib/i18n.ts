export function detectBrowserLanguage(): LangCode {
  if (typeof navigator === 'undefined') return 'en';
  const langs = navigator.languages || [navigator.language];
  for (const raw of langs) {
    const code = raw.split('-')[0].toLowerCase() as LangCode;
    if (code in STRINGS) return code;
  }
  return 'en';
}

export function t(lang: LangCode, key: keyof Strings, vars?: Record<string, string>): string {
  const dict = STRINGS[lang] ?? STRINGS.en;
  let str = dict[key] ?? STRINGS.en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(`{${k}}`, v);
    }
  }
  return str;
}

export function isRTL(lang: LangCode): boolean {
  return SUPPORTED_LANGUAGES.find((l) => l.code === lang)?.rtl ?? false;
}
